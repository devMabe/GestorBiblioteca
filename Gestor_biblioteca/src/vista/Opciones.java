    public void buscar_modificar(){
        int c = 0;
        boolean sw =  false;
        try{
           c = Integer.parseInt(mCedula.getText());
           sw = true;
        }catch(Exception e){
            globalr.setText("Error de tipo de dato debe ser numero");
        }
        
        if(sw){
            UsuarioController uc = new UsuarioController();
            
            ArrayList<String> us = uc.BuscarUsuario(c);
            if(!us.isEmpty()){
                mNombre.setText(us.get(1));
                mFecha.setText(us.get(2));
                if(us.get(3).equals("Femenino")){
                    mSexo.setSelectedIndex(1);
                }else if(us.get(3).equals("Masculino")){
                    mSexo.setSelectedIndex(2);
                }else if(us.get(3).equals("Otro")){
                    mSexo.setSelectedIndex(3);
                }
                if(us.get(4).equals("0")){
                    mEstudiante.setSelected(true);
                }else if(us.get(4).equals("1")){
                    mDocente.setSelected(true);
                }
            }
    
            
        }
    }
