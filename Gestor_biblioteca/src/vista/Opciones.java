    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String  n  ="", cc ="";
        int c = 0, ce=0;
        boolean sw = false;
        CategoriaController ca = new CategoriaController();
        try{
            n = nombre_libro.getText();
            ArrayList<String> lc = ca.BuscarCategoria(cat_libro.getSelectedItem().toString());
            cc = lc.get(0);
            c = Integer.parseInt(cod_libro.getText());
            ce = Integer.parseInt(cod_e_libro.getText());
           
        }catch(Exception e){
            
            sw = true;
        }
        
        if(!sw){
        if( n.equals("") || cc.equals("")){
            globalr.setText("Faltan datos por llenar");
         }else{
            LibroController lc = new LibroController();
                if(c != ce){
                    if(lc.crear(c, n, ce, cc) == 0){
                        globalr.setText("Este libro ya se encuentra registrado");

                    }else{
                        cod_libro.setText("");
                        nombre_libro.setText("");
                        cod_e_libro.setText("");
                        cat_libro.setSelectedIndex(0);
                        globalr.setText("Libro registrado");

                    }
                }else{
                    globalr.setText("Codigo ISBN y codigo Ejemplar deben ser diferentes");
                }
                
            
        }
        }else{
            globalr.setText("Tipo de dato no compatible");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

